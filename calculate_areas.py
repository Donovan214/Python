"""Calculating the area of a square and circle"""

# the function defination of a square


def square(side):
    square = side * side  # calculate the area of the aquare
    # prompting you the area of a square
    print(f"The area of the square is {square} square units.")


square(10)  # the length of a side of the square


# the funcation defination of a circle
def circle(radius):
    circle = 3.14 * radius * radius  # calculating the area of a circle
    # prompting you the area of the circle
    print(f"The area of the circle is {circle} square units.")


circle(20)  # the radius of the circle
